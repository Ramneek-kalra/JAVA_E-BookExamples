public class DatatypeDemo {
    public static void main(String[] args) {
        //Initialization of a byte Variable 'aB'
        byte aB = 10;    
        byte bB = 128;
        byte cB = -128;
        
        //Initialization of a short Variable 'aS' 
        short aS = 10;
        short bS = 32768;
        short cS = -32768;
        
        //Initialization of int Variable 'aI'
        int aI = 10;
        int bI = 2147483648;
        int cI = -2147483648;
    }
}
